class SAItem {
  String word;
  String synonym;
  String antonym;
  String synonym_explaination;
  String antonym_explaination;

  SAItem(String word, String synonym, String antonym) {
    this.word = word;
    this.synonym = synonym;
    this.antonym = antonym;
    synonym_explaination = "synonym_explaination";
    antonym_explaination = "antonym_explaination";
  }
}
